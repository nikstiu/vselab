def z3():
    students = {
        "Петров Иван": ['Русский', 'Английский', 'Французский'],
        "Лебедева Маша": ['Русский', 'Китайский'],
        "Иванов Иван": ['Английский', 'Китайский'],
        "Тихонова Маша": ['Русский'],
    }
    kolvo = set()
    for a in students:
        kolvo.update(students[a])

    sorted_kolvo = sorted(list(kolvo))
    print("Языки, которые знают суденты: ", *sorted_kolvo)

    chinese = []
    for a in students:
        if 'Китайский' in students[a]:
            chinese.append(a)
    print("Студенты, знающие китайский: ",*chinese)

print(z3())